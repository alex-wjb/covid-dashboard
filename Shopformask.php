  <h4 id="howtoprotect"> How to protect yourself </h4>
  <p> “To stop the spread of coronavirus (COVID-19), you should avoid close contact with anyone you do not live with.
    This is called social distancing. It's very important to do what you can to reduce the risk of you and other people
    getting ill with coronavirus. You can spread the virus even if you do not have symptoms. </p>
</div>
<div class="do">
  <h4>Do</h4>
  <ul>
    <li>Try to stay at least 2 metres (3 steps) away from anyone you do not live with (or anyone not in your support
      bubble)</li>
    <li>Wash your hands with soap and water often – do this for at least 20 seconds</li>
    <li>Use hand sanitiser gel if soap and water are not available</li>
    <li>Use hand sanitiser gel if soap and water are not available</li>
    <li>Wash your hands as soon as you get home</li>
    <li>Cover your mouth and nose with a tissue or your sleeve (not your hands) when you cough or sneeze</li>
    <li>Put used tissues in the bin immediately and wash your hands afterwards</li>
    <li>Let fresh air into your home by opening windows, doors and air vents as much as possible</li>
  </ul>
</div>
<div class="dont">
  <h4>Don't</h4>
  <ul>
    <li>Do not touch your eyes, nose or mouth if your hands are not clean</li>
  </ul>
  <h4> Face coverings</h4>
  <p>If you can, wear something that covers your nose and mouth in places where it's hard to stay away from other
    people. </p>
  <p>There are some places where you must wear a face covering, such as:</p>
  <ul>
    <li>On public transport</li>
    <li>In shops</li>
    <li>When you go to hospital appointments or visit someone in hospital</li>
  </ul>
</div>
