<h4> Coronavirus (COVID-19) vaccine</h4>
<p>The coronavirus (COVID-19) vaccine is safe and effective. It gives you the best protection against coronavirus.</p>
<h4>Who can get the COVID-19 Vaccine</h4>
<p>The NHS is currently offering the COVID-19 vaccine to people most at risk from coronavirus.
In England, the vaccine is being offered in some hospitals and pharmacies, at local centres run by GPs and at larger vaccination centres. More centres are opening all the time.
It's being given to:
</p>

<ul>
 <li>people aged 50 and over</li>
<li>people at high risk from coronavirus (clinically extremely vulnerable)</li>
<li>people who live or work in care homes</li>
<li>health and social care workers</li>
<li>people with a condition that puts them at higher risk (clinically vulnerable)</li>
<li>people with a learning disability</li>
<li>people who are a main carer for someone at high risk from coronavirus</li>
</ul>

<p>The order in which people will be offered the vaccine is based on advice from the Joint Committee on Vaccination and Immunisation (JCVI). </p> 
<p>If you are not eligible yet 
Wait to be contacted. The NHS will let you know when it's your turn to have the vaccine. It's important not to contact the NHS for a vaccination before then.
</p>
<h4> How the COVID-19 vaccine is given</h4>
<p> The COVID-19 vaccine is given as an injection into your upper arm.
It's given as 2 doses. You will have the 2nd dose 3 to 12 weeks after having the 1st dose.
You will have 2 doses of the same vaccine.
</p>

<h4> How to get the COVID-19 vaccine</h4>
<p>You can book your vaccination appointments online if any of the following apply:</p>

<ul>
 <li>you are aged 50 or over</li>
<li>you are at high risk from coronavirus (clinically extremely vulnerable)</li>
<li>you are an eligible frontline health or social care worker</li>
<li>you have a condition that puts you at higher risk (clinically vulnerable)</li>
<li>you have a learning disability</li>
<li>you are a main carer for someone at high risk from coronavirus</li>

</ul>

<p>You can book appointments at a larger vaccination centre or a pharmacy that provides COVID-19 vaccinations.</p>
<p>You do not need to wait to be contacted by the NHS.</p>
<h4> How safe is the COVID-19 vaccine? </h4>
<p> The vaccines approved for use in the UK have met strict standards of safety,
quality and effectiveness set out by the independent Medicines and Healthcare products Regulatory Agency (MHRA).</p>

<p> Any coronavirus vaccine that is approved must go through all the clinical trials and safety checks all other licensed medicines go through.
The MHRA follows international standards of safety.</p>

<p> Other vaccines are being developed. They will only be available on the NHS once they have been thoroughly 
tested to make sure they are safe and effective</p>

<p>So far, millions of people have been given a COVID-19 vaccine and reports of serious side effects, 
such as allergic reactions, have been very rare. No long-term complications have been reported.</p>


